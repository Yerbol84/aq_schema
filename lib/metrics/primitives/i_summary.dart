// Протокол summary — перцентили вычисляемые на стороне клиента.
//
// ── Семантика ────────────────────────────────────────────────────────────────
//
// Summary vs Histogram:
//
//   Histogram:
//     + бакеты агрегируются на сервере мониторинга
//     + можно вычислять перцентили по нескольким инстансам
//     - нужно заранее знать диапазон значений для бакетов
//
//   Summary:
//     + точные перцентили без предварительной настройки бакетов
//     - вычисляется на клиенте — overhead на каждый инстанс
//     - нельзя агрегировать перцентили по нескольким инстансам
//
// Summary подходит для:
//   - измерение латентности когда диапазон значений неизвестен заранее
//   - один инстанс сервиса (не нужна агрегация)
//   - когда важна точность перцентилей, а не масштабируемость
//
// ── Сценарии использования ───────────────────────────────────────────────────
//
// СЦЕНАРИЙ 1: Латентность проверки токена (auth-сервис)
//
//   final tokenCheck = metrics.summary(
//     'auth_token_validation_duration_seconds',
//     quantiles: [0.5, 0.9, 0.95, 0.99],
//   );
//
//   final sw = Stopwatch()..start();
//   await validateToken(token);
//   sw.stop();
//   tokenCheck.observe(
//     sw.elapsedMicroseconds / 1e6,
//     attributes: {'method': 'jwt'},
//   );
//
// СЦЕНАРИЙ 2: Размер LLM ответа в токенах
//
//   final tokenCount = metrics.summary(
//     'llm_response_tokens',
//     quantiles: [0.5, 0.95, 0.99],
//   );
//   tokenCount.observe(response.tokens.toDouble());
//
// ── Контракт для реализаций ──────────────────────────────────────────────────
//
// Реализация ДОЛЖНА:
//   - никогда не бросать исключений
//   - быть потокобезопасной
//
// Реализация МОЖЕТ:
//   - игнорировать quantiles если бэкенд их не поддерживает
//   - делегировать в IHistogram если Summary не поддерживается (Prometheus remote)
//   - использовать скользящее окно для вычисления перцентилей

/// Протокол summary метрики.
///
/// Вычисляет перцентили на стороне клиента.
/// Используй когда нужны точные перцентили и нет необходимости
/// агрегировать данные по нескольким инстансам.
abstract interface class ISummary {
  /// Записать наблюдение.
  ///
  /// [value] — измеренное значение.
  /// [attributes] — именованные атрибуты для этого измерения.
  void observe(
    double value, {
    Map<String, String> attributes = const {},
  });
}
