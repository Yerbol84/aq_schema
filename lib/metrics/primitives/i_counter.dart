// Протокол счётчика — монотонно растущее число.
//
// ── Семантика ────────────────────────────────────────────────────────────────
//
// Counter подходит для:
//   - количество событий (запросы, ошибки, логины, коммиты)
//   - количество обработанных единиц (байты, сообщения, задачи)
//
// Counter НЕ подходит для:
//   - текущее состояние (активные соединения → IGauge)
//   - значения которые могут уменьшаться
//
// ── Сценарии использования ───────────────────────────────────────────────────
//
// СЦЕНАРИЙ 1: Простой счётчик без атрибутов
//
//   final requests = metrics.counter('http_requests_total');
//   requests.inc();                    // каждый входящий запрос
//
// СЦЕНАРИЙ 2: Счётчик с атрибутами (самый частый паттерн)
//
//   final runs = metrics.counter('graph_runs_total');
//   runs.inc(attributes: {
//     'project_id': projectId,
//     'status': 'completed',
//   });
//
// СЦЕНАРИЙ 3: Счётчик ошибок с типом
//
//   final errors = metrics.counter('auth_errors_total');
//   errors.inc(attributes: {
//     'error_type': 'invalid_token',
//     'endpoint': '/api/v1/login',
//   });
//
// СЦЕНАРИЙ 4: Счётчик байт (не единицы, а объём)
//
//   final bytes = metrics.counter('network_bytes_sent_total');
//   bytes.inc(value: payload.length.toDouble());
//
// ── Контракт для реализаций ──────────────────────────────────────────────────
//
// Реализация ДОЛЖНА:
//   - никогда не бросать исключений (метрики не должны ломать бизнес-логику)
//   - быть потокобезопасной
//   - игнорировать отрицательные value (counter только растёт)
//   - атрибуты с одинаковыми ключами — последний побеждает
//
// Реализация МОЖЕТ:
//   - агрегировать атрибуты (Prometheus: статические лейблы)
//   - передавать атрибуты per-measurement (OpenTelemetry: dynamic attributes)
//   - игнорировать неизвестные атрибуты

/// Протокол счётчика метрик.
///
/// Монотонно растущее число — только увеличивается, никогда не уменьшается.
/// Сбрасывается только при рестарте процесса.
abstract interface class ICounter {
  /// Увеличить счётчик.
  ///
  /// [value] — на сколько увеличить (по умолчанию 1).
  ///           Отрицательные значения игнорируются.
  /// [attributes] — именованные атрибуты для этого измерения.
  ///                Например: {'project_id': 'proj1', 'status': 'ok'}
  void inc({
    double value = 1,
    Map<String, String> attributes = const {},
  });
}
