// Протокол гистограммы — распределение числовых значений по бакетам.
//
// ── Семантика ────────────────────────────────────────────────────────────────
//
// Histogram подходит для:
//   - длительность операций (запросы, выполнение узлов, LLM вызовы)
//   - размер payload (байты запроса/ответа)
//   - любое значение где важно распределение, а не только среднее
//
// Histogram НЕ подходит для:
//   - подсчёт событий (→ ICounter)
//   - текущее состояние (→ IGauge)
//   - точные перцентили с малым overhead (→ ISummary)
//
// Почему Histogram лучше чем среднее:
//   Среднее время ответа 100мс может скрывать что 5% запросов занимают 2 секунды.
//   Histogram показывает: 50% < 50мс, 95% < 200мс, 99% < 1000мс.
//
// ── Сценарии использования ───────────────────────────────────────────────────
//
// СЦЕНАРИЙ 1: Длительность выполнения графа (через ITimer — предпочтительно)
//
//   // Лучше использовать ITimer — он сам вызывает observe()
//   final timer = metrics.timer('graph_run_duration_seconds');
//   final handle = timer.start(attributes: {'project_id': projectId});
//   await runner.start();
//   handle.stop();   // автоматически записывает длительность
//
// СЦЕНАРИЙ 2: Прямое использование Histogram
//
//   final hist = metrics.histogram(
//     'llm_response_tokens',
//     buckets: [10, 50, 100, 500, 1000, 4000],
//   );
//   hist.observe(
//     response.tokenCount.toDouble(),
//     attributes: {'model': 'gpt-4', 'type': 'completion'},
//   );
//
// СЦЕНАРИЙ 3: Размер HTTP payload
//
//   final payloadSize = metrics.histogram(
//     'http_request_size_bytes',
//     buckets: [100, 1000, 10000, 100000, 1000000],
//   );
//   payloadSize.observe(request.contentLength.toDouble());
//
// ── Контракт для реализаций ──────────────────────────────────────────────────
//
// Реализация ДОЛЖНА:
//   - никогда не бросать исключений
//   - быть потокобезопасной
//   - значения <= 0 записывать в первый бакет (не игнорировать)
//
// Реализация МОЖЕТ:
//   - использовать бакеты переданные при создании через IMetricsService
//   - игнорировать бакеты если бэкенд их не поддерживает (StatsD)
//   - вычислять перцентили на лету (OpenTelemetry ExplicitBucketHistogram)

/// Протокол гистограммы метрик.
///
/// Распределяет наблюдаемые значения по бакетам.
/// Позволяет вычислять перцентили (p50, p95, p99) на стороне бэкенда.
abstract interface class IHistogram {
  /// Записать наблюдение.
  ///
  /// [value] — измеренное значение (обычно секунды или байты).
  /// [attributes] — именованные атрибуты для этого измерения.
  void observe(
    double value, {
    Map<String, String> attributes = const {},
  });
}
